function kToF(k) {
  return k * 3280.84;
}

function kToY(k) {
  return k * 1093.61;
}

function kToMi(k) {
  return k * 0.621371;
}

function kToM(k) {
  return k * 1000;
}

function kToI(k) {
  return k * 39370.1;
}

function kToC(k) {
  return k * 100000;
}

export {kToF, kToY, kToMi, kToM, kToI, kToC};
