function miToF(mi) {
  return mi * 5280;
}

function miToY(mi) {
  return mi * 1760;
}

function miToK(mi) {
  return mi * 1.60934;
}

function miToM(mi) {
  return mi * 1609.34;
}

function miToI(mi) {
  return mi * 63360;
}

function miToC(mi) {
  return mi * 160934;
}

