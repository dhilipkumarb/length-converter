function mToY (m) {
  return m * 1.0936;
}

function mToF(m) {
  return m * 3.2808;
}

function mToMi(m) {
  return m / 1609.34;
}

function mToI(m) {
  return m * 39.3701;
}

function mToK(m) {
  return m / 1000;
}

function mToC(m) {
  return m * 100;
}

export {mToY, mToF, mToMi, mToK, mToI, mToC};
